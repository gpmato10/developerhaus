package com.developerhaus.domain;

public class File {

	private int flSeq;
	private String flNm;
	private String flPth;
	private String flExt;
	private int flSz;
	private int regUsr;
	private String regDt;

	public int getFlSeq() {
		return flSeq;
	}
	public void setFlSeq(int flSeq) {
		this.flSeq = flSeq;
	}
	public String getFlNm() {
		return flNm;
	}
	public void setFlNm(String flNm) {
		this.flNm = flNm;
	}
	public String getFlPth() {
		return flPth;
	}
	public void setFlPth(String flPth) {
		this.flPth = flPth;
	}
	public String getFlExt() {
		return flExt;
	}
	public void setFlExt(String flExt) {
		this.flExt = flExt;
	}
	public int getFlSz() {
		return flSz;
	}
	public void setFlSz(int flSz) {
		this.flSz = flSz;
	}
	public int getRegUsr() {
		return regUsr;
	}
	public void setRegUsr(int regUsr) {
		this.regUsr = regUsr;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	
}
